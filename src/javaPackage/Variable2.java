package javaPackage;

public class Variable2 {
    public static void main(String[] args) {
        String a="hello java";  //String type local variable
        System.out.println(a);
        System.out.println(a.length());//length of string
        System.out.println(a.charAt(3));//prints element of string at that indes
        System.out.println(a.concat("allah"));//concats string with given string
    }
}
